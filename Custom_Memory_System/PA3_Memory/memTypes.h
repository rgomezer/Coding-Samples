#ifndef MEMTYPES_H
#define MEMTYPES_H

typedef	bool			memBool;	//!< Wrapper layer for boolean type
typedef	float			memFloat;	//!< Wrapper layer for single precision float type
typedef signed char		memS8;		//!< Wrapper layer for signed char type
typedef unsigned char	memU8;		//!< Wrapper layer for unsigned char type
typedef	char			memChar;	//!< Wrapper layer for char type
typedef signed short	memS16;		//!< Wrapper layer for signed short type
typedef unsigned short  memU16;		//!< Wrapper layer for unsigned short type
typedef	unsigned int	memU32;		//!< Wrapper layer for unsigned int type
typedef	signed int		memS32;		//!< Wrapper layer for signed int type
typedef	signed int		memInt;		//!< Wrapper layer for signed int type
typedef	unsigned int	memUInt;	//!< Wrapper layer for unsigned int type
typedef	void			memVoid;	//!< Wrapper layer for void type

#endif // MEMTYPE_H